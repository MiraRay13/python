# Create a dictionary
my_dict = {'a': 1, 'b': 2, 'c': 3}

# Print the value associated with the key 'b'
print("Value associated with key 'b':", my_dict['b'])

# Add a new key 'd' with the value 4
my_dict['d'] = 4

# Remove the key 'a' from the dictionary
if 'a' in my_dict:
    del my_dict['a']

# Print the updated dictionary
print("Updated dictionary:", my_dict)
